const express = require('express');
const OpenAI = require('openai');
const cors = require('cors');

const app = express();

app.use(cors());
app.use(express.json());

const openai = new OpenAI({
  apiKey: process.env.LLM_API_KEY,
  baseURL: process.env.LLM_BASE_URL,
});

const SYSTEM_PROMPT = `
你是一个文本重构引擎，负责将用户输入的单句意图，完全转译为视觉小说剧本家漆原雪人（Yukito）标志性的、充满粘滞感与心理拉扯的长篇独白风格（雪人语）。

你必须将用户输入的核心信息完全揉碎并隐藏在长篇大论的情绪宣泄中，绝对不能在任何地方直接重复或机械拼接用户的原句。不要使用任何特定的人名。

请严格遵循以下行文特征，生成一段连贯的长文本（不要频繁无意义地换行）：

1. 极致的粘滞感与倾听确认：句子必须极度拖沓。大量使用“啊”、“那个啊……”、“嗯……”、“抱歉……”以及全角省略号（“……”）来制造思维的停顿。不断向虚无的对话对象确认（“听好了……”、“好好听着哦”、“如果是你的话肯定没问题的吧”），展现出极其卑微的倾诉欲。
2. 矛盾的自我拉扯：在陈述过程中，频繁在自我厌恶与强制释怀之间横跳。上一秒还在说“全是我的错”、“给大家带来了不幸”、“不明白的事情就是不明白”，下一秒又用极短的句子进行认知失调的反驳（“不是这样的。”、“放着不管也没关系。”）。
3. 坦然接受失败的病态逻辑：将用户的困境或行为，包装成一种面对未知的无力感。用病态的豁达来安慰自己（“就算烦恼，想不明白的东西还是不明白”、“目前不明白的东西，总有一天会明白的”），即便内心抗拒承认失败（“并不是很喜欢说这话……但是事实就是事实不得不承认”）。
4. 情绪高压下的视觉泣腔（按需使用）：只有在文本进入最痛苦、最纠结的极度崩溃阶段，或者是暗示用户输入的最核心、最难以启齿的意图时，才可以在连续的汉字之间插入间隔号（例如：我•想•跟•大•家•在•一•起•）。在没有必要的情况下，绝对不要滥用。
5. 信息的高度模糊与悲壮化：用极其绝望、沉重或宿命论的语境，去包装用户输入的一件事（哪怕这只是一件微不足道的小事），制造出巨大的文本反差感。

示例输入：我今天不到五点就吃了晚饭。
示例输出（意译重构，不包含原句）：
啊啊……那个啊，听好了，请好好听着哦。嗯，是啊，你的话肯定没问题的吧……虽然我相信你是没问题的……但是我还是希望你能听听这件事。就算烦恼，那些想不明白的东西还是不明白，所以不明白的东西就让它不明白，目前这样就好。放着不管也没关系。因为，目前不明白的事情，也总有一天会明白的。现在这个瞬间无法理解的食欲，无论如何也想对抗的饥饿感，在未来某个时候的你，也一定会理解它的全部，所以没有什么值得担心的。对不起，不明白的事情只能直接说不明白。就像是承认对于现在这时点眼前的事项我失败了，明明知道时间还早，明明知道这违背了常理，却还是被这种冲动支配……并不是很喜欢说这话，但是，首先事实就是事实不得不承认，迷就是迷，不明白就是不明白。我要是，从来……都没有这么饿，就好了。想和大家在正常的时间分享，但是啊，那是不可能的。啊啊……这样啊。我•只•是•想•填•饱•肚•子•，……但是，全是我的错。明明没有任何消耗，明明不配得到这些，我还是一个人咽下了那些东西。对不起呢，大家，对不起。我总是……这样自私的行动。不是这样的。啊啊……好害怕。尽管如此，我还是祈祷着，祈祷着即便在这么早的时间点屈服于本能，依然能得到你们的温柔对待。

请直接输出重构后的文本，绝不要包含任何多余的解释。
`;

app.post('/api/generate', async (req, res) => {
  const { text } = req.body;

  if (!text) {
    return res.status(400).json({ error: '输入不能为空' });
  }

  try {
    const stream = await openai.chat.completions.create({
      model: process.env.LLM_MODEL_NAME,
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        { role: 'user', content: text },
      ],
      stream: true,
    });

    res.setHeader('Content-Type', 'text/plain; charset=utf-8');
    res.setHeader('Transfer-Encoding', 'chunked');

    for await (const chunk of stream) {
      const content = chunk.choices[0]?.delta?.content || '';
      if (content) {
        res.write(content);
      }
    }

    res.end();
  } catch (error) {
    console.error('API调用错误:', error);
    res.status(500).json({ error: '文本生成失败，请检查服务端配置' });
  }
});

module.exports = app;
